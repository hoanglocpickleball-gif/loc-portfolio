/* ==========================================================================
   CUSTOM CURSOR TRAILING EFFECT
   ========================================================================== */
function initCustomCursor() {
    // Only initialize on desktop/mouse-supported devices
    if (window.matchMedia('(hover: hover) and (pointer: fine)').matches) {
        const cursorDot = document.createElement('div');
        const cursorRing = document.createElement('div');
        cursorDot.className = 'custom-cursor-dot';
        cursorRing.className = 'custom-cursor-ring';
        document.body.appendChild(cursorDot);
        document.body.appendChild(cursorRing);

        let mouseX = 0, mouseY = 0; // Current target coords
        let ringX = 0, ringY = 0;     // Current ring coordinates
        let isMoving = false;

        window.addEventListener('mousemove', (e) => {
            mouseX = e.clientX;
            mouseY = e.clientY;
            
            // Move dot immediately
            cursorDot.style.left = `${mouseX}px`;
            cursorDot.style.top = `${mouseY}px`;

            if (!isMoving) {
                cursorDot.classList.add('active');
                cursorRing.classList.add('active');
                isMoving = true;
            }
        });

        // Trailing interpolation
        const delay = 0.15;
        function animateCursor() {
            ringX += (mouseX - ringX) * delay;
            ringY += (mouseY - ringY) * delay;
            
            cursorRing.style.left = `${ringX}px`;
            cursorRing.style.top = `${ringY}px`;
            
            requestAnimationFrame(animateCursor);
        }
        animateCursor();

        // Hover states for interactive items
        const updateHoverState = () => {
            const interactive = document.querySelectorAll('a, button, .btn, .social-link-item, .contact-item-card, .blog-card, .service-card, .metric-card, input, textarea, .logo, [role="button"]');
            interactive.forEach(el => {
                if (el.dataset.cursorBound) return;
                el.dataset.cursorBound = 'true';

                el.addEventListener('mouseenter', () => {
                    cursorRing.classList.add('hovered');
                    cursorDot.classList.add('hovered');
                });
                el.addEventListener('mouseleave', () => {
                    cursorRing.classList.remove('hovered');
                    cursorDot.classList.remove('hovered');
                });
            });
        };

        updateHoverState();

        // Track dynamically added elements
        const observer = new MutationObserver(updateHoverState);
        observer.observe(document.body, { childList: true, subtree: true });

        // Hide/Show cursor when leaving/entering window viewport
        document.addEventListener('mouseleave', () => {
            cursorDot.classList.remove('active');
            cursorRing.classList.remove('active');
        });
        document.addEventListener('mouseenter', () => {
            cursorDot.classList.add('active');
            cursorRing.classList.add('active');
        });
    }
}

let currentLang = "vi";

function switchLanguage(lang) {
    currentLang = lang;
    document.documentElement.setAttribute("lang", lang);
    localStorage.setItem("portfolio-lang", lang);

    document.querySelectorAll("[data-i18n]").forEach(element => {
        const key = element.getAttribute("data-i18n");
        if (translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });

    // Re-initialize Lucide Icons for newly inserted HTML translations
    if (window.lucide) {
        window.lucide.createIcons();
    }

    // Update Input Placeholders
    const formName = document.getElementById("formName");
    const formEmail = document.getElementById("formEmail");
    const formMsg = document.getElementById("formMsg");
    if (formName) formName.placeholder = lang === "vi" ? "Nhập họ và tên..." : "Enter your full name...";
    if (formEmail) formEmail.placeholder = lang === "vi" ? "Nhập email liên hệ..." : "Enter your contact email...";
    if (formMsg) formMsg.placeholder = lang === "vi" ? "Bạn cần tư vấn chiến dịch gì? Hãy ghi chi tiết tại đây..." : "What campaigns do you need consultation for? Give details here...";

    // Update Lang button label
    const langBtn = document.getElementById("langToggle");
    if (langBtn) {
        langBtn.querySelector("span").textContent = lang === "vi" ? "EN" : "VN";
    }
}

/* ==========================================================================
   THEME TOGGLER (DARK / LIGHT)
   ========================================================================== */
function initTheme() {
    const savedTheme = localStorage.getItem("portfolio-theme") || "dark";
    document.documentElement.setAttribute("data-theme", savedTheme);
    updateThemeIcon(savedTheme);
}

function toggleTheme() {
    const currentTheme = document.documentElement.getAttribute("data-theme");
    const nextTheme = currentTheme === "dark" ? "light" : "dark";
    document.documentElement.setAttribute("data-theme", nextTheme);
    localStorage.setItem("portfolio-theme", nextTheme);
    updateThemeIcon(nextTheme);
}

function updateThemeIcon(theme) {
    const themeToggle = document.getElementById("themeToggle");
    if (themeToggle) {
        if (theme === "light") {
            themeToggle.innerHTML = `<i data-lucide="sun"></i>`;
        } else {
            themeToggle.innerHTML = `<i data-lucide="moon"></i>`;
        }
        if (window.lucide) {
            window.lucide.createIcons();
        }
    }
}

/* ==========================================================================
   TYPING ANIMATION EFFECT
   ========================================================================== */
const typingRoles = {
    vi: ["Performance Marketing", "Digital Marketing", "Thương Mại Điện Tử - UEH"],
    en: ["Performance Marketing", "Digital Marketing", "E-Commerce - UEH"]
};

let roleIndex = 0;
let charIndex = 0;
let isDeleting = false;
let typeTimeout = null;

function typeWriter() {
    const textElement = document.getElementById("typingText");
    if (!textElement) return;

    const currentRoles = typingRoles[currentLang];
    const currentRole = currentRoles[roleIndex];

    if (isDeleting) {
        textElement.textContent = currentRole.substring(0, charIndex - 1);
        charIndex--;
    } else {
        textElement.textContent = currentRole.substring(0, charIndex + 1);
        charIndex++;
    }

    let typeSpeed = 100;
    if (isDeleting) {
        typeSpeed /= 2; // Delete faster
    }

    if (!isDeleting && charIndex === currentRole.length) {
        typeSpeed = 1500; // Pause at end of word
        isDeleting = true;
    } else if (isDeleting && charIndex === 0) {
        isDeleting = false;
        roleIndex = (roleIndex + 1) % currentRoles.length;
        typeSpeed = 500; // Pause before typing next word
    }

    typeTimeout = setTimeout(typeWriter, typeSpeed);
}

/* ==========================================================================
   BACKGROUND CANVAS PARTICLES ANIMATION
   ========================================================================== */
let canvas, ctx;
let particles = [];
const particleCount = 60;
let animationFrameId;

class Particle {
    constructor() {
        this.reset();
    }
    
    reset() {
        this.x = Math.random() * canvas.width;
        this.y = Math.random() * canvas.height;
        this.vx = (Math.random() - 0.5) * 0.4;
        this.vy = (Math.random() - 0.5) * 0.4;
        this.radius = Math.random() * 2 + 1;
        this.alpha = Math.random() * 0.5 + 0.15;
    }
    
    update() {
        this.x += this.vx;
        this.y += this.vy;
        
        if (this.x < 0 || this.x > canvas.width) this.vx = -this.vx;
        if (this.y < 0 || this.y > canvas.height) this.vy = -this.vy;
    }
    
    draw() {
        const theme = document.documentElement.getAttribute("data-theme");
        ctx.beginPath();
        ctx.arc(this.x, this.y, this.radius, 0, Math.PI * 2);
        
        if (theme === "dark") {
            ctx.fillStyle = `rgba(0, 242, 254, ${this.alpha})`;
        } else {
            ctx.fillStyle = `rgba(79, 172, 254, ${this.alpha * 0.4})`; // Muted in light theme
        }
        
        ctx.fill();
    }
}

function initParticles() {
    canvas = document.getElementById("particleCanvas");
    if (!canvas) return;
    
    ctx = canvas.getContext("2d");
    resizeCanvas();
    
    particles = [];
    for (let i = 0; i < particleCount; i++) {
        particles.push(new Particle());
    }
    
    window.addEventListener("resize", resizeCanvas);
    animateParticles();
}

function resizeCanvas() {
    canvas.width = window.innerWidth;
    canvas.height = window.innerHeight;
}

function animateParticles() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    
    const theme = document.documentElement.getAttribute("data-theme");
    
    // Draw lines between close particles
    for (let i = 0; i < particles.length; i++) {
        particles[i].update();
        particles[i].draw();
        
        for (let j = i + 1; j < particles.length; j++) {
            const dx = particles[i].x - particles[j].x;
            const dy = particles[i].y - particles[j].y;
            const dist = Math.sqrt(dx * dx + dy * dy);
            
            if (dist < 100) {
                ctx.beginPath();
                ctx.moveTo(particles[i].x, particles[i].y);
                ctx.lineTo(particles[j].x, particles[j].y);
                
                const opacity = (1 - dist / 100) * 0.15;
                if (theme === "dark") {
                    ctx.strokeStyle = `rgba(0, 242, 254, ${opacity})`;
                } else {
                    ctx.strokeStyle = `rgba(79, 172, 254, ${opacity * 0.3})`;
                }
                
                ctx.lineWidth = 0.5;
                ctx.stroke();
            }
        }
    }
    
    animationFrameId = requestAnimationFrame(animateParticles);
}

/* ==========================================================================
   SCROLL SPY & BACK TO TOP BUTTON
   ========================================================================== */
function handleScroll() {
    const scrollPos = window.scrollY;
    const header = document.querySelector(".header");
    const backToTopBtn = document.getElementById("backToTopBtn");
    
    // Sticky Header Scroll Effect
    if (scrollPos > 50) {
        header.style.padding = "0.75rem 0";
    } else {
        header.style.padding = "1.25rem 0";
    }
    
    // Back to top button visibility
    if (scrollPos > 400) {
        backToTopBtn.classList.add("active");
    } else {
        backToTopBtn.classList.remove("active");
    }
    
    // Scroll Spy for Nav links
    const sections = document.querySelectorAll("section[id]");
    let currentSectionId = "";
    
    sections.forEach(section => {
        const sectionTop = section.offsetTop - 120;
        const sectionHeight = section.offsetHeight;
        if (scrollPos >= sectionTop && scrollPos < sectionTop + sectionHeight) {
            currentSectionId = section.getAttribute("id");
        }
    });
    
    if (currentSectionId) {
        document.querySelectorAll(".nav-link").forEach(link => {
            link.classList.remove("active");
            if (link.getAttribute("href") === `#${currentSectionId}`) {
                link.classList.add("active");
            }
        });
    }
}

/* ==========================================================================
   MOBILE MENU DRAWER CONTROL
   ========================================================================== */
function initMobileMenu() {
    const mobileMenuBtn = document.getElementById("mobileMenuBtn");
    const closeDrawerBtn = document.getElementById("closeDrawerBtn");
    const mobileDrawer = document.getElementById("mobileDrawer");
    const drawerOverlay = document.getElementById("drawerOverlay");
    const drawerLinks = document.querySelectorAll(".drawer-link");
    
    function openDrawer() {
        mobileDrawer.classList.add("active");
        drawerOverlay.classList.add("active");
        document.body.style.overflow = "hidden"; // Disable scroll when drawer open
    }
    
    function closeDrawer() {
        mobileDrawer.classList.remove("active");
        drawerOverlay.classList.remove("active");
        document.body.style.overflow = "auto";
    }
    
    if (mobileMenuBtn) mobileMenuBtn.addEventListener("click", openDrawer);
    if (closeDrawerBtn) closeDrawerBtn.addEventListener("click", closeDrawer);
    if (drawerOverlay) drawerOverlay.addEventListener("click", closeDrawer);
    
    drawerLinks.forEach(link => {
        link.addEventListener("click", closeDrawer);
    });
}

